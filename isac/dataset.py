"""Dataset and data generation for ISAC DL scheduler training.

Feature structure (paper eq. local/global):
    Local  (per-user) : v_k^local  = [Re(h_k)^T, Im(h_k)^T]^T  in R^{2M}
    Global (shared)   : v^global   = [theta_hat_E, CRB(theta_E),
                                       mu, sigma_h^2, rho, P_t/sigma_C^2]^T
                                     in R^6

Label:
    Binary mask y in {0,1}^N  -- 1 for oracle-scheduled users
    Generated by brute-force oracle over all C(N,Kd) subsets

Saved NPZ:
    local   : (S, N, 2*M)
    global_ : (S, 6)
    label   : (S, N)

Reference:
    SecureLEO lab code (dataset.py) — adapted for terrestrial ISAC.
"""
from __future__ import annotations

from pathlib import Path

import numpy as np
from tqdm import tqdm

try:
    import torch
    from torch.utils.data import DataLoader, Dataset, random_split
    TORCH_AVAILABLE = True
except (ImportError, AttributeError):
    TORCH_AVAILABLE = False

from .channel import generate_channels
from .config import SystemConfig
from .sensing import compute_beta_s, run_sensing
from .scheduling import oracle_scheduling_label


# ---------------------------------------------------------------------------
# Feature extraction
# ---------------------------------------------------------------------------

def extract_local_features(H: np.ndarray) -> np.ndarray:
    """Per-user local features (paper eq. local).

    v_k^local = [Re(h_k)^T, Im(h_k)^T]^T  in R^{2M}

    Args:
        H : (M, N) complex channel matrix
    Returns:
        local : (N, 2*M) float32
    """
    M, N  = H.shape
    local = np.zeros((N, 2 * M), dtype=np.float32)
    for n in range(N):
        h_n      = H[:, n]
        local[n] = np.concatenate([h_n.real, h_n.imag])
    return local


def extract_global_features(
    theta_hat: float,
    crb:       float,
    H:         np.ndarray,
    rho:       float,
    snr_db:    float,
    sigma2_C:  float = 1.0,
) -> np.ndarray:
    """Global feature vector (paper eq. global).

    v^global = [theta_hat_E, CRB(theta_E), mu, sigma_h^2, rho, P_t/sigma_C^2]

    Args:
        theta_hat : MLE angle estimate [radians]
        crb       : CRB(theta_E) [radians^2]
        H         : (M, N) user channel matrix
        rho       : power split ratio
        snr_db    : communication SNR [dB]
        sigma2_C  : comm noise variance
    Returns:
        global_ : (6,) float32
    """
    norms    = np.linalg.norm(H, axis=0) ** 2
    mu       = float(np.mean(norms))
    sigma_h2 = float(np.var(norms))
    snr_lin  = 10 ** (snr_db / 10.0)

    return np.array(
        [theta_hat, crb, mu, sigma_h2, rho, snr_lin],
        dtype=np.float32,
    )


# ---------------------------------------------------------------------------
# Single sample generation
# ---------------------------------------------------------------------------

def generate_sample(
    cfg:        SystemConfig,
    P_t:        float,
    snr_db:     float,
    seed:       int,
    beta_s_mag: float,
) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Generate one labeled training sample.

    Pipeline:
        1. Generate channels  (H, g_e, theta_E)
        2. Run sensing        (theta_hat, CRB, g_hat_e)
        3. Oracle scheduling  (label)
        4. Extract features   (local, global_)

    Args:
        cfg        : SystemConfig
        P_t        : comm transmit power [W]
        snr_db     : comm SNR [dB]
        seed       : random seed
        beta_s_mag : |beta_s| round-trip gain
    Returns:
        local   : (N, 2*M)
        global_ : (6,)
        label   : (N,)
    """
    rng = np.random.default_rng(seed)

    # 1. Channel — d_0 for path loss, d_be for Eve channel
    sample = generate_channels(
        M           = cfg.M,
        N           = cfg.N,
        d_0         = cfg.d_0,           # ← path loss reference 
        d_be        = cfg.d_be,          # ← Eve physical distance 
        eta         = cfg.eta,
        d_cu_min    = cfg.d_cu_min,
        d_cu_max    = cfg.d_cu_max,
        beta_e_mag  = cfg.beta_e_mag,
        theta_E_min = cfg.theta_E_min_rad,
        theta_E_max = cfg.theta_E_max_rad,
        seed        = seed,
    )
    H, g_e, theta_E = (
        sample["H"], sample["g_e"], sample["theta_E"]
    )

    # 2. Sensing — uses d_be (Eve physical distance)
    beta_s_t = beta_s_mag * np.exp(
        1j * rng.uniform(0, 2 * np.pi)
    )
    state    = run_sensing(
        theta_E, cfg.M, cfg.M_r, cfg.L,
        cfg.P_s, cfg.sigma2_s, beta_s_t,
        seed=seed,
    )
    theta_hat = state["theta_hat"]
    crb       = state["crb"]
    g_hat_e   = state["at_hat"] * cfg.beta_e_mag

    # 3. Oracle label
    label, _ = oracle_scheduling_label(
        H, g_e, cfg.Kd, g_hat_e,
        P_t, cfg.sigma2_C, cfg.time_frac, cfg.rho,
    )

    # 4. Features
    local   = extract_local_features(H)
    global_ = extract_global_features(
        theta_hat, crb, H, cfg.rho, snr_db, cfg.sigma2_C
    )

    return local, global_, label


# ---------------------------------------------------------------------------
# Dataset generation
# ---------------------------------------------------------------------------

def generate_training_dataset(
    output_path: str | Path,
    cfg:         SystemConfig | None = None,
    num_samples: int | None = None,
    seed:        int = 0,
) -> Path:
    """Generate labeled training dataset and save as NPZ.

    SNR sampled uniformly from [snr_min_dB, snr_max_dB] per sample
    to train scheduler across full SNR range.

    Args:
        output_path : path to save NPZ file
        cfg         : SystemConfig (default: paper Table I values)
        num_samples : override cfg.num_samples if provided
        seed        : base random seed
    Returns:
        output_path : Path to saved NPZ
    """
    if cfg is None:
        cfg = SystemConfig()

    n_samples   = num_samples or cfg.num_samples
    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    # Sensing path gain uses Eve physical distance d_be
    beta_s_mag = compute_beta_s(
        cfg.d_be,           # ← Eve physical distance 
        cfg.f_c,
        cfg.epsilon_dBsm,
    )

    rng_snr    = np.random.default_rng(seed)
    snr_values = rng_snr.uniform(
        cfg.snr_min_dB, cfg.snr_max_dB, size=n_samples
    )

    locals_  = []
    globals_ = []
    labels_  = []

    for n in tqdm(range(n_samples), desc="Generating dataset"):
        snr_db = float(snr_values[n])
        P_t    = 10 ** (snr_db / 10.0) * cfg.sigma2_C

        local, global_, label = generate_sample(
            cfg        = cfg,
            P_t        = P_t,
            snr_db     = snr_db,
            seed       = seed + n,
            beta_s_mag = beta_s_mag,
        )
        locals_.append(local)
        globals_.append(global_)
        labels_.append(label)

    np.savez(
        output_path,
        local   = np.stack(locals_),
        global_ = np.stack(globals_),
        label   = np.stack(labels_),
        N       = cfg.N,
        Kd      = cfg.Kd,
        M       = cfg.M,
    )

    print(f"  Saved {n_samples:,} samples -> {output_path}")
    return output_path


# ---------------------------------------------------------------------------
# PyTorch Dataset
# ---------------------------------------------------------------------------

if TORCH_AVAILABLE:
    class ISACDataset(Dataset):
        """PyTorch Dataset for ISAC DL scheduler."""

        def __init__(self, npz_path: str | Path):
            data            = np.load(npz_path)
            self.local      = data["local"]
            self.global_    = data["global_"]
            self.labels     = data["label"]
            self.N          = int(data["N"])
            self.Kd         = int(data["Kd"])
            self.M          = int(data["M"])
            self.local_dim  = self.local.shape[2]
            self.global_dim = self.global_.shape[1]

        def __len__(self) -> int:
            return self.local.shape[0]

        def __getitem__(self, idx: int):
            return (
                torch.from_numpy(
                    self.local[idx].astype(np.float32)
                ),
                torch.from_numpy(
                    self.global_[idx].astype(np.float32)
                ),
                torch.from_numpy(
                    self.labels[idx].astype(np.float32)
                ),
            )

    def create_data_loaders(
        npz_path:    str | Path,
        batch_size:  int   = 256,
        val_split:   float = 0.1,
        num_workers: int   = 0,
    ) -> tuple[DataLoader, DataLoader]:
        """Create train/val DataLoaders from NPZ."""
        full_ds    = ISACDataset(npz_path)
        val_size   = int(len(full_ds) * val_split)
        train_size = len(full_ds) - val_size
        train_ds, val_ds = random_split(
            full_ds, [train_size, val_size],
            generator=torch.Generator().manual_seed(42),
        )
        train_loader = DataLoader(
            train_ds, batch_size=batch_size,
            shuffle=True, num_workers=num_workers,
            pin_memory=False,
        )
        val_loader = DataLoader(
            val_ds, batch_size=batch_size,
            shuffle=False, num_workers=num_workers,
            pin_memory=False,
        )
        return train_loader, val_loader
